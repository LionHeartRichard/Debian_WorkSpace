package com.oop.geometric;

public class Geometric {
	private short color;
	private int size;
	
	Geometric() {
		System.out.println("Constructor Geometric");
	}
	
	public short getColor() {
		return color;
	}
	public void setColor(short color) {
		this.color = color;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	
	
}
